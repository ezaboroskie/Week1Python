animals=[]

#append adds item to last position in array
animals.append("Cat")
animals.append("Dog")
animals.append("Zebra")

#.insert adds item to specific spot in array
animals.insert(0, "Lion")

#.remove takes specific item from array
animals.remove("Cat")

#remove item based on an index
del animals[0] #removes animal at index 0

print(animals)

